def square_number():
    try:
        # Take input from user
        user_input = input("Enter a number to find its square: ")

        # Convert input to float (to handle both integers and decimals)
        number = float(user_input)

        # Calculate square
        result = number ** 2

        # Display result
        print(f"The square of {number} is {result}")

    except ValueError:
        # Handle case when input is not a number
        print("Error: Please enter a valid number.")

# Call the function
square_number()
